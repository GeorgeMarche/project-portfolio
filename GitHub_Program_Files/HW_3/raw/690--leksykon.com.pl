<h3>KE podpisała umowę na dostawy remdesiviru dla leczenia chorych na Covid-19</h3>
<div class="categories">
<div class="list-group"></div>
</div>
<div class="article-text"><p style="text-align: justify;"><strong>Komisja Europejska poinformowała w czwartek o podpisaniu umowy ramowej dotyczącej wspólnych zamówień na dostawę do 500 tys. dawek remdesiviru, którym leczeni są pacjenci z Covid-19. W kilku krajach UE leku zaczyna już brakować, bo prawie całą produkcję wykupiły USA.</strong></p>
<p style="text-align: justify;">Umowę dotyczącą wspólnych zamówień z firmą farmaceutyczną Gilead podpisało 36 krajów, nie tylko te z UE, ale też państwa Europejskiego Obszaru Gospodarczego, Wielka Brytania oraz kraje Bałkanów Zachodnich. Mogą one teraz składać zamówienia na bezpośrednie zakupy leku Veklury (to nazwa handlowa remdesiviru).</p>
<p style="text-align: justify;">Obecnie to jedyny preparat z warunkowym pozwoleniem na dopuszczenie do obrotu w UE na potrzeby leczenia pacjentów z Covid-19 potrzebujących podawania tlenu. Remdesivir był jednym ze środków, które podano prezydentowi USA Donaldowi Trumpowi w związku z wykryciem u niego koronawirusa.</p>
<p style="text-align: justify;">"Gwarantujemy dziś dostęp do remdesiviru nawet 500 tysiącom pacjentów potrzebujących leczenia. Robimy wszystko, co w naszej mocy, aby zapewnić dostępność bezpiecznego i skutecznego leczenia Covid-19. Dzięki naszym wspólnym unijnym zamówieniom dajemy krajom w całej Europie możliwość łączenia wysiłków i zapewnienia dostępu do niezbędnego sprzętu i leków" - podkreśliła komisarz ds. zdrowia i bezpieczeństwa żywności Stella Kyriakidu.</p>
<p style="text-align: justify;">Podpisanie umowy dotyczącej wspólnych zamówień było możliwe dzięki zawarciu wcześniej przez KE porozumienia z Gilead w sprawie zapewnienia 33 380 dawek leku Veklury. Umowa ta została sfinansowana z instrumentu wsparcia w sytuacjach nadzwyczajnych (ESI). Dostarczane w tych ramach leki są od sierpnia rozprowadzane w UE i Wielkiej Brytanii.</p>
<p style="text-align: justify;">Reuters informował, że w kilku krajach europejskich wystąpiły niedobory leku, którego globalne zapasy zostały prawie w całości zamówione przez Stany Zjednoczone.</p>
