<p style="text-align: justify;">KE podała, że umowa dotycząca wspólnych zamówień zagwarantuje, że państwa będą nadal mogły zaopatrywać się w lek Veklury w sposób nieprzerwany, jeżeli zdecydują się na zakup tego produktu. Gilead poinformował, że rozpocznie realizację zamówień od przyszłego poniedziałku.</p>
<p style="text-align: justify;">Wspólne zamówienia to instrument, który KE wykorzystała na początku tego roku, aby zamawiać środki ochrony indywidualnej, respiratory, materiały do testów lub leków intensywnej terapii.</p>
<p style="text-align: justify;">Z informacji przekazanych przez KE wynika, że przygotowane są kolejne unijne zakupy. 28 września uruchomiona została procedura wspólnych zamówień na dostawę sprzętu medycznego do szczepień przeciwko Covid-19. Obejmuje ona 27 typów urządzeń pogrupowanych w sześciu kategoriach: nośniki szczepionek, pojemniki na odpady, strzykawki, środki odkażające, środki ochrony indywidualnej i materiały do znieczuleń.</p>
<p style="text-align: justify;">Na etapie finalizacji jest procedura wspólnych zamówień na podstawowe leki intensywnej terapii. W najbliższych dniach państwa będą mogły rozpocząć składanie zamówień na leki intensywnej terapii: leki przeciwbólowe, antybiotyki, środki zwiotczające mięśnie, środki znieczulające, leki resuscytacyjne i inne.</p>
